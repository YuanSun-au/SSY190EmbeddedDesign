function multirotor = createMultirotor()
%-------parameters----------
I = [1.71e-005 -1.13e-010 -7.07e-010
	-1.13e-010 1.74e-005 2.12e-010
	-7.07e-010 2.12e-010 3.32e-005]; % from solidworks model and own measurement
mass = 0.0284; % own measurement

% here we mix a X quadcopter. any other mix is possible by adding or
% removing propdrives.
%
% CW        CCW
% 1         4
%  \ front /
%    \   /
%    /   \
%  /       \
% 2         3
%CCW        CW
mixing = 'X';
propdrives=[    createPropDrive(0.032,-0.032,'CCW'),...
                createPropDrive(-0.032,-0.032,'CW'),...
                createPropDrive(-0.032,0.032,'CCW'),...
                createPropDrive(0.032,0.032,'CW')];

multirotor = struct( 'Roll',0,...
            'Pitch',0,...
            'Yaw',0,...
            'Omega',[2; 1.9; 0.5],...
            'Pos',[0; 0; 0],...
            'Vel',[0; 0; 0],...
            'Mass',mass,...
            'Inertia',I,...
            'PropDrives',propdrives,...
            'Mixing', mixing);

end